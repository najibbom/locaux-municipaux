    private void jButton17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton17MouseClicked
        // TODO add your handling code here:
        Integer numUsager = Integer.parseInt(jTextField3.getText());
        Integer numSallee = Integer.parseInt(jTextField4.getText());
        String horraire = (String) jComboBox1.getSelectedItem();

        try
        {
            con = MySqlConnect.ConnectDB();
            String qr = "select * from Salle WHERE numSalle="+numSallee;
            Statement st3 = con.createStatement();
            ResultSet rs3 = st3.executeQuery(qr);
            
            
            rs3.next();
            if(horraire == "Matin") {
                if(rs3.getInt("reservationMatin") == 1) {
                    JOptionPane.showMessageDialog(null, "Salle déja réserver.", "Erreur", JOptionPane.ERROR_MESSAGE);
                } else {
                    Statement st4 = con.createStatement();
                    int result  = st4.executeUpdate("update Salle SET reservationMatin = 1 WHERE numSalle="+numSallee);
                    JOptionPane.showMessageDialog(null, "Salle réserver avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
                }
            } else { 
                if(horraire == "Aprés Midi") {
                    if(rs3.getInt("reservationApresMidi") == 1) {
                        JOptionPane.showMessageDialog(null, "Salle déja réserver.", "Erreur", JOptionPane.ERROR_MESSAGE);
                    } else {
                        Statement st4 = con.createStatement();
                        int result  = st4.executeUpdate("update Salle SET reservationApresMidi = 1 WHERE numSalle="+numSallee);
                        JOptionPane.showMessageDialog(null, "Salle réserver avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    if(horraire == "Soirée") {
                        if(rs3.getInt("reservationSoiree") == 1) {
                            JOptionPane.showMessageDialog(null, "Salle déja réserver.", "Erreur", JOptionPane.ERROR_MESSAGE);
                        } else {
                            Statement st4 = con.createStatement();
                            int result  = st4.executeUpdate("update Salle SET reservationSoiree = 1 WHERE numSalle="+numSallee);
                            JOptionPane.showMessageDialog(null, "Salle réserver avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
                        }   
                    }
                }
            }
            
            
        } catch(SQLException ex){
            Logger lgr = Logger.getLogger(login.class.getName());
            lgr.log(Level.SEVERE, ex.getMessage(), ex);
        }
        
    }//GEN-LAST:event_jButton17MouseClicked


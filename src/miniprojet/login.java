    public ArrayList<Salle> listeSalles() {
    ArrayList<Salle> listeSalles = new ArrayList<>();
    
    try{
    con = MySqlConnect.ConnectDB();
    String query2 = "select * from Salle";
    Statement st2 = con.createStatement();
    ResultSet rs2 = st2.executeQuery(query2);

    Salle salle;
    
    while(rs2.next()) {
        salle = new Salle(rs2.getInt("numSalle"), rs2.getInt("reservationMatin"), rs2.getInt("reservationApresMidi"), rs2.getInt("reservationSoiree"));
        listeSalles.add(salle);
    }
    
    } catch(Exception ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
    
    return listeSalles;
}
    
    public void afficherSalles() {
        ArrayList<Salle> list = listeSalles();
        DefaultTableModel model = (DefaultTableModel)consult_table.getModel();
        Object[] row = new Object[6];
        for(int i=0; i<list.size(); i++) {
            row[0] = list.get(i).getNumSalle();
            row[1] = list.get(i).getReservationMatin();
            row[2] = list.get(i).getReservationApresMidi();
            row[3] = list.get(i).getReservationSoiree();
            model.addRow(row);
        }
    }

    

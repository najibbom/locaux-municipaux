    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        MaterialUIMovement.add(jButton2, MaterialColors.DEEP_PURPLE_50);
        //Connection
            
            String username = jTextField1.getText();
            String password = jPasswordField1.getText();

        try
        {
            int log = 1;
            con = MySqlConnect.ConnectDB();
            st = (Statement)con.createStatement();
            rs = st.executeQuery("select * from Compte");
            
            while(rs.next()) 
            {
                if(rs.getString(3).equals(username) && rs.getString(4).equals(password)) 
                {
                    log = 0;
                    break;
                }
            }
                if(log == 0) {
                    jFrame6.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "username ou mot de passe incorrect, veuillez les corriger.", "Connnectoin failed", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    jTextField1.grabFocus();
                }

        } catch(SQLException ex){
            Logger lgr = Logger.getLogger(login.class.getName());
            lgr.log(Level.SEVERE, ex.getMessage(), ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

